    private void tblcontrolstockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcontrolstockMouseClicked
        // TODO add your handling code here:
        int pos = this.tblcontrolstock.getSelectedRow();
        if (pos >= 0) {
            ControlStock control = this.tablastock.getValue(pos);
            objselect=control;
            this.cboAlmacen.setSelectedItem(control.getAlmacen().getDescripcion());
            this.cboInsumo.setSelectedItem(control.getInsumo().getNombre());
            this.txtprecio.setText(control.getPrecioventa() + "");
            this.txtstock.setText(control.getStock() + "");
            this.controlsave = "modificar";
        }
    }//GEN-LAST:event_tblcontrolstockMouseClicked
