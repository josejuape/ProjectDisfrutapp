    private void btnGuardarControlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarControlActionPerformed
        try {
            // TODO add your handling code here:

            int pi = this.cboInsumo.getSelectedIndex();
            int pa = this.cboAlmacen.getSelectedIndex();
            if (pi > -1 && pa > -1 && !this.txtstock.getText().equals("") && !this.txtprecio.getText().equals("")) {
                ControlStock control = new ControlStock();
                control.setInsumo(this.modelCboInsumo.getElement(pi));
                control.setAlmacen(this.modelCboAlmacen.getElement(pa));
                control.setStock(Double.parseDouble(this.txtstock.getText()));
                control.setPrecioventa(Double.parseDouble(this.txtprecio.getText()));
                if (this.controlsave.equalsIgnoreCase("guardar")) {
                    control.setTipoOperacion("i");
                    GestionControlStock gestionControl = new GestionControlStock();
                    gestionControl.registrar(control);
                    JOptionPane.showMessageDialog(null, "Registrado correctamente.");
                    this.tablastock.setData(gestionControl.listar());
                    this.tblcontrolstock.setModel(tablastock);
                    this.tablastock.fireTableDataChanged();
                } else {
                    if (this.controlsave.equalsIgnoreCase("modificar")) {
                        control.setIdcontrol(objselect.getIdcontrol());
                        control.setTipoOperacion("a");
                        GestionControlStock gestionControl = new GestionControlStock();
                        gestionControl.actualizar(control);
                        JOptionPane.showMessageDialog(null, "Actualizado correctamente.");
                        this.tablastock.setData(gestionControl.listar());
                        this.tblcontrolstock.setModel(tablastock);
                        this.tablastock.fireTableDataChanged();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Llene todos los campos!");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PanelControlStock.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(PanelControlStock.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarControlActionPerformed
